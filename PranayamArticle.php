            <h3>Pranayama: yogic breath-work</h3>
            <p> Your breath is your most intimate companion. It is the essential interface between your body and your mind. Your breath both responds to and controls your emotions as well as your thoughts.</p>
            <p>Yogic breathing exercises are called “pranayama”. The literal translation of this Sanskrit word is “control of the prana”. Prana is the vital energy, or life force, that is known in Chinese as “chi” and Japanese “ki”. It is the subtle energy that is used and manipulated in acupuncture, shiatsu, reiki, tai chi, reflexology and many Eastern martial arts.
</p>
            <p>People, often unknowingly, use the power of prana in daily life. When your child falls, you may rush over to “kiss” it where it hurts, thus transferring prana. If your friend is sick, you may gently stroke his head, thereby transmitting prana. When you bang your knee, you instinctively hold your breath and place both of your hands on the injury in an unconscious attempt to bring additional prana to the area.</p>

             <p>With regular practice, pranayama enables you to more consciously control the non-physical subtle energy within your own being. Pranayama enables you to cleanse and strengthen your physical body while calming your mind.</p>
<h3>Why do pranayama?</h3>
             <p>“Pranayama is the link between the mental and physical disciplines of yoga. While the action is physical, the effect is to make the mind calm, lucid and steady.” - Swami Vishnu-devananda, Complete Illustrated Book of Yoga</p>

             <p>Controlling your own mind is perhaps one of the most difficult things you can do in life. After observing the intimate connection between the mind and the breath, ancient yogis developed a series of breath techniques that would enable them to control their minds.
</p>

             <p>If you watch a person who is at peace, engaged in deep thought, you will notice that his/her breath is slow and even; it may sometimes even be suspended for short periods. You have also probably noticed that when your mind is affected by negative emotions, your breathing becomes fast, irregular and unsteady. These observations indicate the interdependence and interaction of breath and mind.</p>


             <p>By its very nature your mind tends to be unsteady. It is constantly affected by the images it sees, hears and experiences through your senses. Pranayama helps you to make your mind one-pointed, so that you can feel more peaceful.</p>
